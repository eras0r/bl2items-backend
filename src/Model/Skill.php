<?php

namespace Bl2\Model;

use Doctrine\ORM\Mapping as ORM;
use JMS\Serializer\Annotation as Serializer;

/**
 * Entity object representing a skill.
 * @ORM\Entity
 * @ORM\Table(name="skill", uniqueConstraints={@ORM\UniqueConstraint(name="unique_name", columns={"name"})})
 **/
class Skill extends AbstractEntity {

    /**
     * @ORM\Column(type="string")
     * @var string
     **/
    protected $name;

    /**
     * @ORM\Column(type="string")
     * @var string
     **/
    protected $text;

    /**
     * @ORM\Column(type="string")
     * @var string
     **/
    protected $image;

    /**
     * @ORM\Column(type="integer")
     * @var int
     **/
    protected $levels;

    /**
     * @ORM\Column(type="boolean")
     * @Serializer\SerializedName("killSkill")
     * @var boolean
     **/
    protected $killSkill;

    /**
     * Creates a new character class by initializing is properties by using the values given in the associative array.
     *
     * @param array $data associative array holding the properties for the character class.
     */
    public function __construct(array $data) {
        parent::__construct($data);
    }

    /**
     * @param string $image
     */
    public function setImage($image) {
        $this->image = $image;
    }

    /**
     * @return string
     */
    public function getImage() {
        return $this->image;
    }

    /**
     * @param int $levels
     */
    public function setLevels($levels) {
        $this->levels = $levels;
    }

    /**
     * @return int
     */
    public function getLevels() {
        return $this->levels;
    }

    /**
     * @param string $name
     */
    public function setName($name) {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName() {
        return $this->name;
    }

    /**
     * @param string $text
     */
    public function setText($text) {
        $this->text = $text;
    }

    /**
     * @return string
     */
    public function getText() {
        return $this->text;
    }

    /**
     * @param boolean $killSkill
     */
    public function setKillSkill($killSkill) {
        $this->killSkill = $killSkill;
    }

    /**
     * @return boolean
     */
    public function getKillSkill() {
        return $this->killSkill;
    }

    protected function doValidation() {
        parent::doValidation(); // TODO: Change the autogenerated stub
    }
}